reduceDefaultResults = function(reg, ids) {

  res.list = reduceResultsList(
    reg = reg, 
    ids = ids,
    fun = function(job, res) {
      
      info = getJobInfo(reg, job$id)[1:3]
      values = cbind(info, res)

      return(values)
  })
  return(res.list)
}

